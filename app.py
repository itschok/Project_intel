# One-Hot Encoding ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö loan_purpose (‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç sparse ‡πÄ‡∏õ‡πá‡∏ô sparse_output)
encoder = OneHotEncoder(sparse_output=False, drop='first')
